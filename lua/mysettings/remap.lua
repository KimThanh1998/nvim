vim.g.mapleader = " "

--Open file explorer
vim.keymap.set("n", "<leader>pv", vim.cmd.Ex)

--Move highlight lines up/down
vim.keymap.set("v", "J", ":m '>+1<CR>gv=gv")
vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv")

--Append beneath line to current lines and not moving cursor to the end of line
vim.keymap.set("n", "J", "mzJ`z")

--Move up and down but cursor is in middle
vim.keymap.set("n", "<C-d>", "<C-d>zz")
vim.keymap.set("n", "<C-u>", "<C-u>zz")

--Search string but cursor is in middle
vim.keymap.set("n", "n", "nzzzv")
vim.keymap.set("n", "N", "Nzzzv")

